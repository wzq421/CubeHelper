package com.example.myapplication.bean.rxbus;

public class MainFrg_To_MainAct {
    private int width;
    private int height;

    public MainFrg_To_MainAct(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
